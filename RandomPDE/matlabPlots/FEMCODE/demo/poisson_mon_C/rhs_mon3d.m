function [ out ] = rhs_mon3d(x, der)
%% RHS FUNCTION FOR NONLINEAR MONOTONE PROBLEM

if (nargin == 1) || (0 == sum(der))
    
    t1 = pi .* x(:,1);
    t2 = cos(t1);
    t3 = t2 .^ 2;
    t4 = pi .^ 2;
    t5 = t3 .* t4;
    t6 = x(:,2) .^ 2;
    t7 = 1 - x(:,2);
    t8 = t7 .^ 2;
    t10 = pi .* x(:,3);
    t11 = sin(t10);
    t12 = t11 .^ 2;
    t16 = sin(t1);
    t17 = t16 .* t7;
    t19 = t16 .* x(:,2);
    t21 = t17 .* t11 - t19 .* t11;
    t23 = t16 .^ 2;
    t24 = t23 .* t6;
    t25 = cos(t10);
    t26 = t25 .^ 2;
    t28 = t8 .* t26 .* t4;
    t31 = 0.1e1 + 0.64e2 .* t5 .* t6 .* t8 .* t12 + 0.64e2 .* t21 .^ 2 + 0.64e2 .* t24 .* t28;
    t32 = t31 .^ (-0.125e1);
    t33 = t4 .* pi;
    t40 = t2 .* pi;
    t41 = t7 .* t11;
    t61 = t31 .^ (-0.25e0);
    t63 = (0.1e1 + t61) .* t16;
    t65 = x(:,2) .* t7;
    t99 = t25 .* pi;
    out = 0.200e1 .* t32 .* (-0.128e3 .* t2 .* t33 .* t6 .* t8 .* t12 .* t16...
        + 0.128e3 .* t21 .* (t40 .* t41 - t40 .* x(:,2) .* t11) + 0.128e3...
        .* t16 .* t6 .* t8 .* t26 .* t33 .* t2) .* t2 .* pi .* x(:,2) .* t41...
        + 0.16e2 .* t63 .* t4 .* t65 .* t11 + 0.200e1 .* t32 .* (0.128e3 .* t5...
        .* x(:,2) .* t8 .* t12 - 0.128e3 .* t5 .* t6 .* t7 .* t12 - 0.256e3...
        .* t21 .* t16 .* t11 + 0.128e3 .* t23 .* x(:,2) .* t28 - 0.128e3 .* t24...
        .* t7 .* t26 .* t4) .* t21 + 0.16e2 .* t63 .* t11 + 0.200e1 .* t32 .*...
        (0.128e3 .* t3 .* t33 .* t6 .* t8 .* t11 .* t25 + 0.128e3 .* t21 .*...
        (t17 .* t99 - t19 .* t99) - 0.128e3 .* t24 .* t8 .* t25 .* t33 .* t11)...
        .* t16 .* t65 .* t99;

else 
    out = zeros(size(x,1), 1);
end

end